        // Slider functionality
        let sliderPositions = {};

        function moveSlider(sliderId, direction) {
            const slider = document.getElementById(sliderId);
            const slides = slider.querySelectorAll('.slide');
            const slideWidth = slides[0].offsetWidth;

            if (!sliderPositions[sliderId]) {
                sliderPositions[sliderId] = 0;
            }

            sliderPositions[sliderId] += direction * slideWidth;

            // Reset to beginning if we've gone too far
            const maxPosition = -(slides.length - 3) * slideWidth;
            if (sliderPositions[sliderId] < maxPosition) {
                sliderPositions[sliderId] = 0;
            }
            if (sliderPositions[sliderId] > 0) {
                sliderPositions[sliderId] = maxPosition;
            }

            slider.style.transform = `translateX(${sliderPositions[sliderId]}px)`;
        }

        // Initialize sliders
        document.addEventListener('DOMContentLoaded', function() {
            const sliders = document.querySelectorAll('.slider');
            sliders.forEach(slider => {
                sliderPositions[slider.id] = 0;
            });

            // Sign in modal functionality
            const signInBtn = document.getElementById('signInBtn');
            const signInModal = document.getElementById('signInModal');
            const closeModal = document.querySelector('.close');

            if (signInBtn && signInModal) {
                signInBtn.addEventListener('click', function(e) {
                    e.preventDefault();
                    signInModal.style.display = 'block';
                });

                closeModal.addEventListener('click', function() {
                    signInModal.style.display = 'none';
                });

                window.addEventListener('click', function(e) {
                    if (e.target === signInModal) {
                        signInModal.style.display = 'none';
                    }
                });
            }


            // Sign up modal functionality
            const signUpBtn = document.getElementById('signUpBtn');
            const signUpModal = document.getElementById('signUpModal');
            const close = document.querySelector('#closeSignUp');

            if (signUpBtn && signUpModal) {
                signUpBtn.addEventListener('click', function(e) {
                    e.preventDefault();
                    signUpModal.style.display = 'block';
                });

                close.addEventListener('click', function() {
                    signUpModal.style.display = 'none';
                });

                window.addEventListener('click', function(e) {
                    if (e.target === signUpModal) {
                        signUpModal.style.display = 'none';
                    }
                });
